export function getNamesByPersonality(
    personality: string,
    count: number = 5,
): DogName[] {
    const filtered = dogNames.filter((dog) =>
        dog.personalities.includes(personality),
    );
    const shuffled = filtered.sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
}

